# affichage de chaque lettre d'un mot en utilisant for
mot = input("Entrez un mot : ")
for lettre in mot:
    print(lettre)

# affichage de chaque element d'une liste avec une boucle for

print("Affichage de chaque élément de la liste :")
liste=["hello","world","python",12,3.14]
for element in liste:
    print(element)
